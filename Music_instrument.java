import java.util.List;

public class Music_instrument {
    public List<Guitar> Guitars;
    public List<Piano> Pianos;
    public List<Trumpet> Trumpets;

    public List<Guitar> getGuitars() {
        return Guitars;
    }

    public void setGuitars(List<Guitar> Guitars) {
        this.Guitars = Guitars;
    }

    public List<Piano> getPianos() {
        return Pianos;
    }

    public void setPianos(List<Piano> pianos) {
        this.Pianos=Pianos;
    }

    public List<Trumpet> getTrumpets() {
        return Trumpets;
    }

    public void setTrumpets(List<Trumpet> trumpets) {
        this.Trumpets =Trumpets;
    }
}





